package org.compiere.process;

public class NotaFiscalVO {

	private String id;
	private String nf;
	private String bpname;
	private String email;
	private String idParceiro;
	private String pedido;
	private String dataEmissao;
	private String dataTransicao;
	private String protocolo;
	private String chaveAcesso;
	private String valorTotal;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNf() {
		return nf;
	}
	public void setNf(String nf) {
		this.nf = nf;
	}
	public String getBpname() {
		return bpname;
	}
	public void setBpname(String bpname) {
		this.bpname = bpname;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getIdParceiro() {
		return idParceiro;
	}
	public void setIdParceiro(String idParceiro) {
		this.idParceiro = idParceiro;
	}
	public String getPedido() {
		return pedido;
	}
	public void setPedido(String pedido) {
		this.pedido = pedido;
	}
	public void setDataEmissao(String dataEmissao) {
		this.dataEmissao = dataEmissao;
	}
	public String getDataEmissao() {
		return dataEmissao;
	}
	public void setDataTransicao(String dataTransicao) {
		this.dataTransicao = dataTransicao;
	}
	public String getDataTransicao() {
		return dataTransicao;
	}
	public void setProtocolo(String protocolo) {
		this.protocolo = protocolo;
	}
	public String getProtocolo() {
		return protocolo;
	}
	public void setChaveAcesso(String chaveAcesso) {
		this.chaveAcesso = chaveAcesso;
	}
	public String getChaveAcesso() {
		return chaveAcesso;
	}
	public void setValorTotal(String valorTotal) {
		this.valorTotal = valorTotal;
	}
	public String getValorTotal() {
		return valorTotal;
	}
	
		
	
}
